public class MyCalendar {
	int year;
	int month;
	int day;
	int hour;
	int minute;
	
	public void setYear (int year) {
		this.year = year;
	}
	
	public int getYear(){
		return this.year;
	}
	
	public void setMonth (int month) {
		this.month = month;
	}
	
	public int getMonth() {
		return this.month;
	}
	
	public void setDay(int day) {
		this.day = day;
	}
	
	public int getDay() {
		return this.day;
	}
	
	public void setHour (int hour) {
		this.hour = hour;
	}
	
	public int getHour() {
		return this.hour;
	}
	
	public void setMinute (int minute) {
		this.minute = minute;
	}
	
	public int getMinute() {
		return this.minute;
	}
}
